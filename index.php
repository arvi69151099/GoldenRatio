                            <h3> 1. Fibonacci Retracements</h3>
                             <p style="font-size:18px;text-align: justify; ">
                             
Fibonacci retracements use horizontal lines to indicate areas of support or resistance. Levels are calculated using the high and low points of the chart. Then five lines are drawn: the first at 100% (the high on the chart), the second at 61.8%, the third at 50%, the fourth at 38.2%, and the last one at 0% (the low on the chart). After a significant price movement up or down, the new support and resistance levels are often at or near these lines.<br></p>
<h3>2. Fibonacci Arcs</h3>
<p style="font-size:18px;text-align: justify; ">
Finding the high and low of a chart is the first step to composing Fibonacci arcs. Then, with a compass-like movement, three curved lines are drawn at 38.2%, 50%, and 61.8% from the desired point. These lines anticipate the support and resistance levels, as well as trading ranges.


