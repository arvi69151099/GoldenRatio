How does this relate to design? You can find the Golden Ratio when you divide a line into two parts and the longer part (a) divided by the smaller part (b) is equal to the sum of (a) + (b) divided by (a), which both equal 1.618. This formula can help you when creating shapes, logos, layouts, and more.</p>
